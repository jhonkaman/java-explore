// Creating Arrays

// Run: java 05-arrays/01-creating-arrays.java

// This example demonstrates how to create arrays in Java

public class CreatingArrays {
    public static void main(String[] args) {
        int[] numbers = {1, 2, 3, 4, 5};
        String[] colors = {"red", "green", "blue"};
        System.out.println("Array of numbers: " + numbers.length + " elements");
        System.out.println("Array of colors: " + colors.length + " elements");
    }
}

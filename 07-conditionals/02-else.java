// Else Statement

// Run: java 07-conditionals/02-else.java

// This example demonstrates else statement for alternative code execution

public class Else {
    public static void main(String[] args) {
        int score = 45;
        if (score >= 60) {
            System.out.println("You passed");
        } else {
            System.out.println("You failed");
        }
    }
}

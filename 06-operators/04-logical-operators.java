// Logical Operators

// Run: java 06-operators/04-logical-operators.java

// This example demonstrates logical operators (&&, ||, !)

public class LogicalOperators {
    public static void main(String[] args) {
        boolean x = true;
        boolean y = false;
        System.out.println("x && y: " + (x && y));
        System.out.println("x || y: " + (x || y));
        System.out.println("!x: " + (!x));
        System.out.println("!y: " + (!y));
    }
}

// Number Variables

// Run: java 03-numbers/01-number-variables.java

// This example demonstrates creating and using number variables

public class NumberVariables {
    public static void main(String[] args) {
        int age = 25;
        double height = 5.9;
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
    }
}
